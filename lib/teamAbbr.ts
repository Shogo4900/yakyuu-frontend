const ONE_CHAR_VALUES = new Set(Object.values(TEAM_ONE_CHAR));

/**
 * 与えられたチーム名（表記ゆれ含む）から一文字表記を返す。
 * マッピングに存在しない場合は、元の文字列の先頭1文字にフォールバックする。
 */
export function toOneChar(teamName: string | null | undefined): string {
  if (!teamName) return "?";
  const trimmed = teamName.trim();

  // スポナビ側が既に一文字表記（例: NPBのスコア表は「巨」「ヤ」等）で返してくる場合、そのまま使う
  if (trimmed.length === 1 && ONE_CHAR_VALUES.has(trimmed)) return trimmed;

  if (TEAM_ONE_CHAR[trimmed]) return TEAM_ONE_CHAR[trimmed];

  // 完全一致しない場合、部分一致で探す（「読売ジャイアンツ(予)」のような余分な文字列対策）
  for (const [key, value] of Object.entries(TEAM_ONE_CHAR)) {
    if (trimmed.includes(key)) return value;
  }

  return trimmed.charAt(0) || "?";
}
